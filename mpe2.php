<!-- ¿Cuales son los objetivos específicos de la maestría en psicología educativa?

Comprender, interpretar los propósitos y metodología de la psicología educativa con base en el conocimiento de las principales corrientes psicológicas.

Diseñar y desarrollar programas de intervención educativa para favorecer el desarrollo de niños, adolescentes, jóvenes y adultos.

Desarrollar competencias profesionales para el diseño, ejecución y evaluación de programas educativos orientados al diagnóstico, asesoría y orientación en las instituciones educativas.


Perfil de egreso

Conoce e interpreta las diversas corrientes teóricas de la psicología y del aprendizaje en las que se fundamenta la psicología educativa.

Emplea los modelos y planteamientos de la psicología educativa para diagnosticar y proporcionar tratamiento a dificultades de aprendizaje y problemas emocionales y de la conducta.

Diseña implementa y evalúa programas de orientación educativa y profesional, planes de acción tutorial y de mediación escolar y resolución de conflictos.

Propone y formula alternativas y modelos para el diseño de ambientes de aprendizajes y de asesoría a los centros escolares para el mejoramiento del aprovechamiento escolar.

Diferencia, clasifica, y relaciona métodos de investigación educativa y de medición en psicología y educación para el diagnóstico y evaluación psicológica. -->

